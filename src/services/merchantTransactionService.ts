  /**
   * Get merchant balance (sum of credit - debit)
   */
  async getMerchantBalance(merchantId: string) {
    try {
      const transactions = await prisma.merchantTransaction.findMany({
        where: {
          merchantId,
          status: TransactionStatus.Completed,
        },
      });

      const totalCredit = transactions.reduce(
        (sum, t) => sum + Number(t.credit),
        0
      );
      const totalDebit = transactions.reduce(
        (sum, t) => sum + Number(t.debit),
        0
      );
      const balance = totalCredit - totalDebit;

      // Group by transaction type
      const balanceByType: Record<
        string,
        {
          transactionType: MerchantTransactionType;
          balance: number;
          totalCredit: number;
          totalDebit: number;
          transactionCount: number;
        }
      > = {};

      Object.values(MerchantTransactionType).forEach((type) => {
        balanceByType[type] = {
          transactionType: type,
          balance: 0,
          totalCredit: 0,
          totalDebit: 0,
          transactionCount: 0,
        };
      });

      transactions.forEach((transaction) => {
        const type = transaction.transactionType;
        const credit = Number(transaction.credit);
        const debit = Number(transaction.debit);

        balanceByType[type].totalCredit += credit;
        balanceByType[type].totalDebit += debit;
        balanceByType[type].balance += credit - debit;
        balanceByType[type].transactionCount += 1;
      });

      const result = Object.values(balanceByType).filter(
        (item) => item.transactionCount > 0
      );

      return {
        success: true,
        data: {
          merchantId,
          balance,
          totalCredit,
          totalDebit,
          transactionCount: transactions.length,
          balanceByType: result,
        },
      };
    } catch (error: any) {
      return { success: false, error: error.message };
    }
  }


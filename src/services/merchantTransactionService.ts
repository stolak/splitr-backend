  /**
   * Create a new merchant transaction
   */
  async createMerchantTransaction(input: CreateMerchantTransactionInput) {
    try {
      // Validate credit and debit
      if (input.credit > 0 && input.debit > 0) {
        throw new Error(
          "Transaction cannot have both credit and debit amounts"
        );
      }

      if (input.credit === 0 && input.debit === 0) {
        throw new Error("Transaction must have either credit or debit amount");
      }

      // Verify merchant exists
      const merchant = await prisma.merchant.findUnique({
        where: { id: input.merchantId },
      });

      if (!merchant) {
        throw new Error("Merchant not found");
      }

      // Verify invoice exists if provided
      if (input.invoiceRef) {
        const invoice = await prisma.invoice.findUnique({
          where: { id: input.invoiceRef },
        });

        if (!invoice) {
          throw new Error("Invoice not found");
        }

        // Verify invoice belongs to merchant
        if (invoice.merchantId !== input.merchantId) {
          throw new Error("Invoice does not belong to this merchant");
        }
      }

      const transaction = await prisma.merchantTransaction.create({
        data: {
          liftpayId: "", // Will be auto-generated by trigger
          merchantId: input.merchantId,
          invoiceRef: input.invoiceRef,
          credit: input.credit,
          debit: input.debit,
          transactionType: input.transactionType,
          transactionDate: input.transactionDate,
          description: input.description,
          status: input.status || TransactionStatus.Pending,
        },
        include: {
          merchant: {
            select: {
              id: true,
              liftpayId: true,
              businessName: true,
              businessEmail: true,
            },
          },
          invoice: {
            select: {
              id: true,
              liftpayId: true,
              amount: true,
              status: true,
            },
          },
        },
      });

      return { success: true, data: transaction };
    } catch (error: any) {
      return { success: false, error: error.message };
    }
  }


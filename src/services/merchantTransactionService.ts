  /**
   * Get all transactions for a merchant
   */
  async getMerchantTransactionsByMerchantId(
    merchantId: string,
    filters?: {
      transactionType?: MerchantTransactionType | MerchantTransactionType[];
      status?: TransactionStatus;
      startDate?: Date;
      endDate?: Date;
      page?: number;
      limit?: number;
    }
  ) {
    try {
      const page = filters?.page || 1;
      const limit = filters?.limit || 10;
      const skip = (page - 1) * limit;

      const where: any = { merchantId };
      if (filters?.transactionType) {
        // Support both single value and array of values
        if (Array.isArray(filters.transactionType)) {
          where.transactionType = { in: filters.transactionType };
        } else {
          where.transactionType = filters.transactionType;
        }
      }
      if (filters?.status) where.status = filters.status;

      // Date range filtering
      if (filters?.startDate || filters?.endDate) {
        where.transactionDate = {};
        if (filters?.startDate) {
          where.transactionDate.gte = filters.startDate;
        }
        if (filters?.endDate) {
          where.transactionDate.lte = filters.endDate;
        }
      }

      const [transactions, total] = await Promise.all([
        prisma.merchantTransaction.findMany({
          where,
          skip,
          take: limit,
          include: {
            invoice: {
              select: {
                id: true,
                liftpayId: true,
                amount: true,
                status: true,
              },
            },
          },
          orderBy: [{ transactionDate: "desc" }, { createdAt: "desc" }],
        }),
        prisma.merchantTransaction.count({ where }),
      ]);

      return {
        success: true,
        data: {
          transactions,
          pagination: {
            total,
            page,
            limit,
            totalPages: Math.ceil(total / limit),
          },
        },
      };
    } catch (error: any) {
      return { success: false, error: error.message };
    }
  }


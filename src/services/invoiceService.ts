  calculateRefund({
    downpayment = 0,
    principalPaymentToDate = 0,
    approvalDate = new Date(),
    loanStartDate,
    currentBalance = 0,
    unpaidInterest = 0,
    unpaidPenalty = 0,
  }: {
    downpayment?: number;
    principalPaymentToDate?: number;
    approvalDate: Date | string;
    loanStartDate: Date | string;
    currentBalance?: number;
    unpaidInterest?: number;
    unpaidPenalty?: number;
  }) {
    try {
      const REFUND_RATE_PERCENTAGE = 1;
      const INTEREST_RATE = 7.5;

      // Validate approval date
      const approvedDate = new Date(approvalDate);

      if (isNaN(approvedDate.getTime())) {
        throw new Error("Invalid approvalDate provided");
      }
      const loanCommencementDate = new Date(loanStartDate);
      if (isNaN(loanCommencementDate.getTime())) {
        throw new Error("Invalid loanStartDate provided");
      }

      // Difference in days
      const diffInMs = approvedDate.getTime() - loanCommencementDate.getTime();
      const diffInDays = Math.max(0, Math.floor(diffInMs / (1000 * 60 * 60 * 24)));

      // Remainder of (difference / 30)
      const applicableDays = diffInDays % 30;

      // Calculations
      const assumedRefundable = Number(principalPaymentToDate) + Number(downpayment);

      const refundCharge = assumedRefundable * REFUND_RATE_PERCENTAGE * 0.01;

      const interestCharge = applicableDays > 5 ? (currentBalance * INTEREST_RATE * 0.01) / 30 : 0;

      const refundDue =
        assumedRefundable -
        (refundCharge + interestCharge + Number(unpaidInterest) + Number(unpaidPenalty));

      return {
        success: true,

        calculation: {
          assumedRefundable,
          refundCharge,
          applicableDays,
          interestCharge,
          unpaidInterest,
          unpaidPenalty,
          refundDue,
        },
        summary: {
          message: "Refund calculation completed successfully",
          approvalDate: approvedDate.toISOString(),
          loanStartDate: loanCommencementDate.toISOString(),
          totalDeductions:
            refundCharge + interestCharge + Number(unpaidInterest) + Number(unpaidPenalty),
        },
      };
    } catch (error: any) {
      return {
        success: false,
        message: error.message || "Failed to calculate refund",
      };
    }
  }

  /**
   * Get all invoices with optional filters
   */
  async getAllInvoices(filters?: {
    merchantId?: string;
    buyerId?: string;
    status?: InvoiceStatus;
    customerEmail?: string;
    q?: string;
    type?: InvoiceType;
    page?: number;
    limit?: number;
  }) {
    try {
      const page = filters?.page || 1;
      const limit = filters?.limit || 10;
      const skip = (page - 1) * limit;

      const where: any = {};
      if (filters?.merchantId) where.merchantId = filters.merchantId;
      if (filters?.buyerId) where.buyerId = filters.buyerId;
      if (filters?.status) where.status = filters.status;
      if (filters?.customerEmail) where.customerEmail = filters.customerEmail;
      if (filters?.type) where.type = filters.type;
      if (filters?.q) {
        where.OR = [
          { splitrId: { contains: filters.q } },
          { customerName: { contains: filters.q } },
          { customerPhoneNumber: { contains: filters.q } },
          { customerEmail: { contains: filters.q } },
        ];
      }

      const [invoices, total] = await Promise.all([
        prisma.invoice.findMany({
          where,
          skip,
          take: limit,
          include: {
            items: true,
            buyer: {
              select: {
                id: true,
                splitrId: true,
                firstName: true,
                lastName: true,
                email: true,
              },
            },
            merchant: {
              select: {
                id: true,
                splitrId: true,
                businessName: true,
                businessEmail: true,
              },
            },
            loan: {
              select: {
                id: true,
                downPaymentAmount: true,
                loanAmount: true,
                loanTenure: true,
                monthlyRepayment: true,
              },
            },
          },
          orderBy: { createdAt: "desc" },
        }),
        prisma.invoice.count({ where }),
      ]);

      return {
        success: true,
        data: {
          invoices,
          pagination: {
            total,
            page,
            limit,
            totalPages: Math.ceil(total / limit),
          },
        },
      };
    } catch (error: any) {
      return {
        success: false,
        message: error.message || "Failed to fetch invoices",
      };
    }
  }


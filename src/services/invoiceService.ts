  /**
   * Create a new invoice with items
   */
  async createInvoice(input: CreateInvoiceInput) {
    try {
      // Validate merchant exists
      const merchant = await prisma.merchant.findUnique({
        where: { id: input.merchantId },
      });

      if (!merchant) {
        throw new Error("Merchant not found");
      }

      // Validate buyer if provided
      if (input.buyerId) {
        const buyer = await prisma.buyer.findUnique({
          where: { id: input.buyerId },
        });

        if (!buyer) {
          throw new Error("Buyer not found");
        }
      }

      // Validate items
      if (!input.items || input.items.length === 0) {
        throw new Error("Invoice must have at least one item");
      }

      // Create invoice with items
      const invoice = await prisma.invoice.create({
        data: {
          splitrId: "", // Will be auto-generated by trigger
          customerName: input.customerName,
          customerEmail: input.customerEmail,
          customerPhoneNumber: input.customerPhoneNumber,
          dueDate: new Date(input.dueDate),
          note: input.note,
          amount: input.amount,
          buyerId: input.buyerId,
          merchantId: input.merchantId,
          status: input.status || InvoiceStatus.Pending,
          type: input.type || InvoiceType.Purchase,
          items: {
            create: input.items.map((item) => ({
              description: item.description,
              quantity: item.quantity,
              amount: item.amount,
            })),
          },
        },
        include: {
          items: true,
          buyer: {
            select: {
              id: true,
              splitrId: true,
              firstName: true,
              lastName: true,
              email: true,
            },
          },
          merchant: {
            select: {
              id: true,
              splitrId: true,
              businessName: true,
              businessEmail: true,
            },
          },
          loan: {
            select: {
              id: true,
            },
          },
        },
      });

      // Send invoice email to customer
      try {
        const formattedDueDate = new Date(invoice.dueDate).toLocaleDateString("en-US", {
          year: "numeric",
          month: "long",
          day: "numeric",
        });

        const formattedItems = invoice.items.map((item) => ({
          description: item.description,
          quantity: item.quantity,
          amount: Number(item.amount).toLocaleString("en-US", {
            minimumFractionDigits: 2,
            maximumFractionDigits: 2,
          }),
        }));

        const formattedTotal = Number(invoice.amount).toLocaleString("en-US", {
          minimumFractionDigits: 2,
          maximumFractionDigits: 2,
        });
        emailService.sendTemplateEmail({
          to: invoice.customerEmail,
          templateName: "invoiceCreated",
          subject: `New Invoice from ${invoice.merchant.businessName} - ${invoice.splitrId}`,
          data: {
            customer_name: invoice.customerName,
            merchant_name: invoice.merchant.businessName,
            merchant_email: invoice.merchant.businessEmail,
            invoice_id: invoice.splitrId,
            due_date: formattedDueDate,
            items: formattedItems,
            total_amount: formattedTotal,
            note: invoice.note || "",
            invoice_link: `${
              process.env.FRONTEND_URL || "https://splitr.co"
            }/buyer/dashboard/invoices`,
          },
        });
      } catch (emailError: any) {
        // Log email error but don't fail the invoice creation
        console.error("Failed to send invoice email:", emailError.message);
      }

      return {
        success: true,
        message: "Invoice created successfully",
        data: invoice,
      };
    } catch (error: any) {
      return {
        success: false,
        message: error.message || "Failed to create invoice",
      };
    }
  }

